let sumar = (num1,num2)=>{
    return num1 + num2;
}

let restar = (num1,num2)=>{
    return num1 - num2;
}

let dividir = (num1,num2)=>{
    return num1 / num2;
}

let multiplicar = (num1,num2)=>{
    return num1 * num2;
}


console.log(sumar(4,5))
console.log(restar(4,5))
console.log(dividir(20,5))
console.log(multiplicar(4,10))